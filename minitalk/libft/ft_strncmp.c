#include "libft.h"

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	size_t			i;
	unsigned char	*s11;
	unsigned char	*s22;

	s11 = (unsigned char *)s1;
	s22 = (unsigned char *)s2;
	i = 0;
	if (n == 0)
		return (0);
	while (s11[i] == s22[i] && (i < n - 1))
		i++;
	return (s11[i] - s22[i]);
}
